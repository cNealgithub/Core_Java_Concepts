package javaplayground;

public class Type_ConvAndCast {

	public static void main(String[] args) {
		// <-----------Tpe Conversion And Type Casting----------->
		
		byte by = 47;
		int num = 257;
		//Type Conversion 
		int num1 = by;
		//Type Casting:-Explecit Type Conversion
		byte by1 = (byte)num;
		
		System.out.println(num1);
		System.out.print(by1);

	}

}
